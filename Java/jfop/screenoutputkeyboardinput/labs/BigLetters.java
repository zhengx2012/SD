public class BigLetters {
  public static void main(String[] args) {
    System.out.println("X       X TTTTTTT ZZZZZZZ");
    System.out.println(" X     X     T         Z");
    System.out.println("  X  X       T       Z");
    System.out.println("    X        T      Z");
    System.out.println("  X  X       T     Z");
    System.out.println(" X     X     T    Z");
    System.out.println("X       X    T   ZZZZZZZZ");
  }

}
