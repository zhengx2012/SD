public class LetterHouse {
  public static void main(String[] args) {
    roof();
    base();
  }
  public static void roof() {
    System.out.println("                 ZZZ");
    System.out.println("      TTTTTTTTTT Z Z");
    System.out.println("    T            TZ Z");
    System.out.println("  T                TZ");
    System.out.println(" T                   T");
    System.out.println("T                      T");
  }
  public static void base() {
    System.out.println(" X XXXX         XXXX X");
    System.out.println(" X X  X  ZZZZZ  X  X X");
    System.out.println(" X XXXX  Z   Z  XXXX X");
    System.out.println(" X       ZZ  Z       X");
    System.out.println(" X       Z   Z       X");
    System.out.println(" XXXXXXXXXXXXXXXXXXXXX");
  }
}
